# Week-Long Flask App Challenge
# Create a simple web application using Flask that allows users to manage a list of books.
# The app should include features for adding, viewing, editing, and deleting books.

# Requirements
# 1 - Basic Flask Setup: Set up a Flask project with a virtual environment. https://flask.palletsprojects.com/en/3.0.x/installation/
# 2 - CRUD Operations: Implement Create, Read, Update, and Delete operations for books.
# 3 - CSV Integration: Use the sample CSV file to read, store, delete and update book information.  
# 4 - API Endpoints: Provide RESTful API endpoints for CRUD operations with flask. https://flask.palletsprojects.com/en/3.0.x/quickstart/
# 5 - Use postman to test the API endpoints.
# 6 - Postman allows you to save the api endpoints to reuse them later. - Do that!

