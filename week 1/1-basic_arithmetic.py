# Write a Python program that asks the user for two numbers and then prints
# the sum, difference, product, and quotient of those numbers.


# Hint: You can use the input() function to get user input. For example:
num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

sum = num1 + num2
difference = num1 - num2
product = num1*num2
quotient = num1/num2

print("The sun of the two numbers is ", sum) 
print("The difference of the two numbers is ", difference)
print("The product of the two numbers is ", product)
print("The quotient of the two numbers is ", quotient)